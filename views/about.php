<div class="t2">
	<h2>About</h2>
</div>

<p class="paragraph">
Welcome to the Nintendo Community Fangame Convention! The NCFC is a community led fangaming event where fangame and indie game creators from all across the world can show off their games for five days. The event is entirely online, and includes several other events such as workshops, small contests and video playthroughs of the games.
</p>

<div class="t2">
	<h2>History</h2>
</div>

<p class="paragraph">
The NCFC started in 2007 as an organized effort by Shadowman and DJ Yoshiman, and backed up by a select few individuals. It was the first successful Nintendo-related version of SAGE, meant to be inspired by that particular event while allowing for more Nintendo-oriented items to be shown. It started off with a length of four days, a radio, some workshops and two events. Since then, the event has climbed up and opened to more sponsors, events, booths, and even people within the video game development industry. 
</p>

